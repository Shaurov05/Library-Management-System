    public void ShowInfo(){
        
        if(jComboBox1.getSelectedItem() == "Book ID"){
            try{     
            String c = jTextField1.getText();
            String sql1 = "select Issue.Book_ID,Issue.Name, Issue.Student_ID,Issue.S_Name, Issue.Department,Issue.Date_of_Issue from Issue Inner Join Student on Issue.Book_ID = Student.Student_ID where Issue.Book_ID = ?";
            
            pst = conn.prepareStatement(sql1);
            pst.setString(1, jTextField1.getText());
            rs = pst.executeQuery();
            count = info.getRowCount();
            
            info.setModel(DbUtils.resultSetToTableModel(rs));
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null , e);
        }
        }
        
        else{
            
        try{            
            String sql1 = "select Issue.Book_ID,Issue.Name, Issue.Student_ID,Issue.S_Name, Issue.Department,Issue.Date_of_Issue from Issue Inner Join Student on Issue.Student_ID = Student.Student_ID";
            
            pst = conn.prepareStatement(sql1);
            rs = pst.executeQuery();
            info.setModel(DbUtils.resultSetToTableModel(rs));
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null , e);
        }
    }
        if(count>1){
            Clear();
            JOptionPane.showMessageDialog(null,"There are multiple records with this Book ID.So,Check in the table or search by Student ID");
        }
        
    }
