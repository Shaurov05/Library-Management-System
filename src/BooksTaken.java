    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        count = 0;
        String sql;
        String c =(String)jComboBox1.getSelectedItem();
        
        
        if(c == "Student ID"){           
            sql = "select * from Issue where Student_ID = ?";
        }
        else{  
            sql = "select * from Issue where Book_ID = ?";            
        }
 
        try{
            pst = conn.prepareStatement(sql);
            pst.setString(1, jTextField1.getText());
            rs = pst.executeQuery();
            if(rs.next()){
                String add1 = rs.getString("Name");
                jTextField2.setText(add1);
                String add2 = rs.getString("Edition");
                jTextField3.setText(add2);
                String add3 = rs.getString("S_Name");
                jTextField4.setText(add3);
                String add4 = rs.getString("Department");
                jTextField5.setText(add4);
                String add5 = rs.getString("Year");
                jTextField6.setText(add5);
                String add6 = rs.getString("Term");
                jTextField7.setText(add6);
                String add7 = rs.getString("Date_Of_Issue");
                jTextField8.setText(add7);
                ShowInfo();
                rs.close();
                pst.close();
            }
            else{
                JOptionPane.showMessageDialog(null, "No Issue Record with this ID");
            }
        }      
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                rs.close();
                pst.close();
            }
            catch(Exception e){
                
            }
        }       
    }//GEN-LAST:event_SearchActionPerformed

